# Escreva uma função que receba o lado de um quadrado e retorne sua área (A = lado²).

# DEFININDO FUNÇÃO
# -- Função calculando a área de um quadrado
def area_quadrado(a):
    return a ** 2


# --- PROGRAMA PRINCIPAL --- #

# Monstrando resultado das áreas utilizando a função area_quadrado
print(area_quadrado(4))
print(area_quadrado(9))
